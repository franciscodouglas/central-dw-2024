function testeFOR(){
    //i = incremento ou incremental
    //i = i+1; (ou i++)
    for(let i=10;i<10; i++ ){
        console.log(i);
    }

}

function testeWhile(){
    let i = 0;
    while (i<10){
        console.log(i)
        i++
    }
}

function testeDO(){
    let i = 1
    do{
        console.log(i)
        i++
    }while (i<10)

}

